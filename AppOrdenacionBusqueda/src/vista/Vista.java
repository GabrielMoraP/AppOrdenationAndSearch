    private void btnBynaryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBynaryActionPerformed
        if (jTextField1.getText() == "") {
            JOptionPane.showMessageDialog(null, "Ingrese un ISBN", "ERROR", JOptionPane.ERROR_MESSAGE);
            jTextField1.requestFocus();
        } else {
            String isbn = jTextField1.getText();
            long startTime = System.nanoTime();

            BinarySearch binarySearch = new BinarySearch();

            long endTime = System.nanoTime();
            long totalTime = endTime - startTime;
            lblBinary.setText(totalTime + " nanoTime");
            Libro[] aux = libroOrdenado.clone();
            int i = binarySearch.bSearch(aux, isbn);
            if (i != -1) {
                libroConsulta(aux, i);
            } else {
                JOptionPane.showMessageDialog(null, "Elemento no encontrado", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnBynaryActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            libros = IOAcceso.readFromFile("recursos/libros");
            if (libros == null) {
                JOptionPane.showMessageDialog(null, "No hay datos en el archivo");
            }
            libroOrdenado = libros.clone();
            int n = libroOrdenado.length;
            QuickSort quickSort = new QuickSort();
            libroOrdenado = quickSort.sortIsbn(libroOrdenado, 0, n - 1);
            actualizarLibros();
        } catch (FileNotFoundException e1) {
            JOptionPane.showMessageDialog(null, "Error de acceso al archivo");
        }
    }//GEN-LAST:event_formWindowOpened

    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        if (libros != null) {
            try {
                IOAcceso.writeToFile(libros);
            } catch (FileNotFoundException e1) {
                JOptionPane.showMessageDialog(null, "Error de acceso al archivo de salida");
            }
        } else {
            JOptionPane.showMessageDialog(null, "No hay datos que guardar al archivo");
        }
    }//GEN-LAST:event_formWindowClosing

