    public void actualizarLibros() {
        Object[] datos = new Object[tablaUno.getColumnCount()];
        DefaultTableModel modelo = (DefaultTableModel) tablaUno.getModel();
        modelo.setRowCount(0);
        String infoLibros = "";
        Libro[] librosInfo = libros.clone();
        int i = 0;
        while (i < libros.length) {
            infoLibros += librosInfo[i].toString() + "\n";
            i++;
        }
        String datosEntrada = infoLibros;
        try (Scanner entrada = new Scanner(datosEntrada)) {
            while (entrada.hasNext()) {
                String datosPrestamo = entrada.nextLine();
                String[] subcadenas = datosPrestamo.split("\t");
                datos[0] = subcadenas[0];
                datos[1] = subcadenas[1];
                datos[2] = subcadenas[2];
                datos[3] = subcadenas[3];
                datos[4] = subcadenas[4];
                datos[5] = subcadenas[5];
                modelo.addRow(datos);
            }
        }
    }

